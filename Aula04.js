// Operadores JavaScript

// + = ADIÇÂO
// - = SUBTRAÇÃO
// / = DIVISÃO
// * = MULTIPLICAÇÃO

const resultado = 7 + 3;
console.log(resultado);

const resultadoSubtracao = 9 - 5;
console.log(resultadoSubtracao);

const resultadoDivisao = 25 / 5;
console.log(resultadoDivisao);

const resultadoMultiplicacao = 8 * 8;
console.log(resultadoMultiplicacao);

// Podemos usar esses operadores em outras situaçoes, como incrementar uma frase por exemplo.

const text1 = 'Olá, meu nome é Matheus lima ';
const text2 = 'Eu sou aluno da Obra social som bosco '; 

const textoFinal = text1 + text2;
console.log(textoFinal);